#include "push_swap.h"

long	ft_atoi_s(const char *s)
{
	long	n;
	int		sign;

	n = 0;
	sign = 1;
	while (*s == ' ' || (*s > 8 && *s < 14))
		s++;
	if ((*s == '+') || (*s == '-'))
		if (*s++ == '-')
			sign *= -1;
	while (*s >= '0' && *s <= '9')
	{
		n = n * 10 + (*s - '0');
		s++;
		if (n > 2147483648 && sign == -1)
			return (3000000000);
		if (n > 2147483647 && sign == 1)
			return (3000000000);
	}
	return (n * sign);
}
